def RecipeToFile(recipe_name, ingredients, file):
    file.write(recipe_name + "\n")
    file.write(str(len(ingredients)) + "\n")
    for ingredient in ingredients:
        file.write(f"{ingredient['ingredient_name']} | {ingredient['quantity']} | {ingredient['measure']}\n")
    file.write("\n")


with open('Рецепты.txt', 'w', encoding='utf-8') as file:
    for recipe, ingredients in cook_book.items():
        RecipeToFile(recipe, ingredients, file)

